puts 1 > 2
puts 1 < 2
# Outputs: false, true
puts "*******"
puts 5 >= 5
puts 5 <= 4
# Outputs: true, false
puts "*******"
# == means 'Are these equal?' and != means "Are these different?"
puts 1 == 1
puts 2 != 1
# Outputs true, true.
puts "*******"
# Strings can be compared too. But they use dictionary ordering. cat comes first.
# but capital letters come before lowercase letters.
puts 'cat' < 'dog'
# Output true
puts 'cat' < 'Dog'
# Output false
puts 'Cat' < 'dog'
# Output true
puts "*******"
